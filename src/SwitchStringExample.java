
public class SwitchStringExample {

	
	
	
	public static void main(String[] args)
	{
	String testString = "yes";
	switch (testString)
	{
	case "yes":
	System.out.println("value was yes");
	break;
	case "no":
	System.out.println("value was no");
	break;
	} //end switch
	
	
	} //end main
} //end class
